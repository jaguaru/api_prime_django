class RoomSerializer(serializers.HyperlinkedModelSerializer):
      class Meta:
            model = Room
            fields = ("room_code", "name", "capacity", "status", "created", "updated")


####--------------------------------------------------------------------------------------------------------------
####--------------------------------------------------------------------------------------------------------------
class EventSerializer(serializers.HyperlinkedModelSerializer):
      class Meta:
            model = Event
            fields = ("event_code", "name", "event_type", "status", "fk_room_code", "fk_customer_code", "created", "updated")

####--------------------------------------------------------------------------------------------------------------
####--------------------------------------------------------------------------------------------------------------
class CustomerSerializer(serializers.HyperlinkedModelSerializer):
      class Meta:
            model = Customer
            fields = ("customer_code", "phone", "name", "lastname", "status", "created", "updated")


